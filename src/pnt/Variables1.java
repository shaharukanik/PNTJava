package pnt;

public class Variables1 {
    public static void main(String[] args) {

        int num1 = 8;
        System.out.println(num1);
        num1 = 19;
        System.out.println(num1);

        int num2 = num1 - 9;
        System.out.println(num2);
        System.out.println(num2 - num1);

        double d1 = 12.3;
        System.out.println(d1);

        d1 = 11.0/3.0;
        System.out.println(d1);

    }
}
