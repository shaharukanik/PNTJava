package pnt;

public class Variables2 {
    public static void main(String[] args) {

        int num1 = 9;
        System.out.println(num1);

        num1 = 10;
        num1 = num1 + num1;
        System.out.println(num1);

        double num2 = num1 + 3.5;
        System.out.println(num2);

        num2 = 2.0+6.0*5.0/2.0;
        System.out.println(num2);

        boolean a = true;
        boolean b = false;
        System.out.println(a);
        System.out.println(b);
    }
}